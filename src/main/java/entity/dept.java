package entity;

public class dept {
    private String dept_no;
    private String dept_name;
    private String dept_loc;
    private String dept_manager;

    public String getDept_no() {
        return dept_no;
    }

    public void setDept_no(String dept_no) {
        this.dept_no = dept_no;
    }

    public String getDept_name() {
        return dept_name;
    }

    public void setDept_name(String dept_name) {
        this.dept_name = dept_name;
    }

    public String getDept_loc() {
        return dept_loc;
    }

    public void setDept_loc(String dept_loc) {
        this.dept_loc = dept_loc;
    }

    public String getDept_manager() {
        return dept_manager;
    }

    public void setDept_manager(String dept_manager) {
        this.dept_manager = dept_manager;
    }
}
